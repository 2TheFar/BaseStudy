import java.util.*;

public class LeftSideView {
    /**
     * 这个是右视图的孪生姐妹篇
     * 因为右视图最快的做法其实是DFS，不需要遍历完整棵树，只需要把每一层遍历的第一个节点记录到结果集里面就可以了
     * 写完了才发现这个是错的呀！！！一样要遍历完整棵树，因为树的高度是不清楚的呀！！！不遍历完怎么知道最左边会不会更下一层楼呢？
     * 误解ClaudeCode的意思了......他说的其实是：理论上都是 O(n)，但 DFS 常数更小、空间更省，实际运行会快一点。
     * 结果来说也确实比BFS快，能做到100%了，就当是掌握两种解法，顺便复习一下DFS吧
     * 
     * 前提不是先左后右，而是先右后左，这样可以保证每一层遍历的第一个节点都是最右边的
     * 证明：
     * 假设有同一层的任意两个节点A和B，A在左边，B在右边
     * 他们的最近公共祖先（LCA）为X
     * 可以看成A和B一起从根节点出发，然后在X这个岔路口分道扬镳，各奔东西了（see you again~）
     * 也可以看成A和B一起往上找相同祖先，各自一条向上延伸的线，然后终于在X这个地方相遇了，X就是这个交叉点
     * （这两个节点一定在某一棵子树里，最小子树的根节点就是LCA，再小一点就拆成左右两棵子树，一个在左子树一个在右子树，没有交叉）
     * 
     * 那么因为是先右后左遍历，所以肯定先遍历X的右子树，再遍历X的左子树
     * 所以就是先遍历右边的B，再遍历左边的A，也就是说同一层的节点中，一定是右边的先遍历，左边的后遍历
     * 因此，最右边的节点最先被遍历
     * （想象一棵完全二叉树，而不是一棵不确定的树，能让自己好受一些。还有想象的顺序不是往上找，而是往下找，也能更舒服一些）
     * 
     * 那么怎么判断当前节点是不是这一层第一个遍历的呢？
     * 从0开始看：
     * 当结果集有0个节点时，那深度为0的节点还没有被遍历过（但凡有一个结果集都不为0了），所以如果当前节点深度是0的话，那就是第一层第一个，应该放入结果集中
     * 当结果集有1个节点时，那深度为1的节点还没有被遍历过（但凡有一个结果集都不为1了），所以如果当前节点深度是1的话，那就是第二层第一个，应该放入结果集中
     * 如此类推
     * 所以用DFS递归的方法可以更快地找出右视图
     * 
     * 其实从先右后左变成先左后右，右视图就变成了左视图，右视图已经用另一种方法写过了，所以这里就写左视图
     * 还有一点小发现，就是先右后左遍历，在把一整棵树翻转之后，就变成了先左后右遍历
     */
    public List<Integer> leftSideView(TreeNode root) {
        List<Integer> result = new ArrayList();
        DFS(root, 0, result);
        return result;
    }

    private void DFS(TreeNode root,int depth,List<Integer> result) {
        if (root == null)
            return;
        if (depth == result.size()) {
            result.add(root.val);
        }
        DFS(root.left, depth + 1, result);
        DFS(root.right, depth+1, result);
    }
}
